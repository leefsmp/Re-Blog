
import ClientAPI from 'ClientAPI'
import BaseSvc from './BaseSvc'

export default class PostsSvc extends BaseSvc {

  /////////////////////////////////////////////////////////////////
  //
  //
  /////////////////////////////////////////////////////////////////
  constructor (opts) {

    super (opts)

    this.api = new ClientAPI(this._config.apiUrl)
  }

  /////////////////////////////////////////////////////////////////
  //
  //
  /////////////////////////////////////////////////////////////////
  name() {

    return 'PostsSvc'
  }

  /////////////////////////////////////////////////////////////////
  //
  //
  /////////////////////////////////////////////////////////////////
  getPosts () {

    const url = this._config.apiUrl

    return this.api.ajax(url)
  }
}
